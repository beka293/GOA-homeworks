# 1. საყვარელი ფერები
ფერები = ["ლურჯი", "წითელი", "მწვანე"]
print("1)", ფერები)

# 2. კვირის დღეები
დღეები = ["ორშაბათი", "სამშაბათი", "ოთხშაბათი", "ხუთშაბათი", "პარასკევი", "შაბათი", "კვირა"]
print("2)", დღეები)

# 3. 5 რიცხვი → მესამე (ინდექსი 3)
რიცხვები1 = [10, 20, 30, 40, 50]
print("3)", რიცხვები1[3])  # 40

# 4. 5 ქალაქი → შეცვალე მეორე (ინდექსი 2)
ქალაქები = ["თბილისი", "ბათუმი", "ქუტაისი", "რუსთავი", "გორი"]
ქალაქები[2] = "ფოთი"
print("4)", ქალაქები)

# 5. ოჯახი → პირველი(0) და ბოლო(-1)
ოჯახი = ["მამა", "დედა", "მე", "და"]
print("5)", ოჯახი[0], "და", ოჯახი[-1])

# 6. 6 რიცხვი → მეოთხე(4) = 100
რიცხვები2 = [1, 2, 3, 4, 5, 6]
რიცხვები2[4] = 100
print("6)", რიცხვები2)

# 7. 10 ძაღლი → მეხუთე(5) სხვა ძაღლით
ძაღლები = ["რექსი", "ბოლო", "ლუნა", "მაქსი", "ბელა", "ჯექსი", "როზი", "ჩარლი", "სკაი", "ჩორნა"]
ძაღლები[5] = "შარკი"
print("7)", ძაღლები)

# 8. სტრინგი+რიცხვი+ბულეანი → ერთი ელემენტი
სხვადასხვა = ["ტექსტი", "სიტყვა", 123, 456, True, False]
print("8)", სხვადასხვა[4])  # True

# 9. მუსიკოსები → თუ პირველი 'Drake'
მუსიკოსები = ["Drake", "Eminem", "Post Malone", "Travis Scott", "Bad Bunny"]
if მუსიკოსები[0] == "Drake":
    მუსიკოსები[0] = "კარტი ემ"
print("9)", მუსიკოსები)

# 10. რიცხვები → პირველი+ბოლო +1000
რიცხვები3 = [5.5, 10, 15.2, 20]
რიცხვები3[0] += 1000
რიცხვები3[-1] += 1000
print("10)", რიცხვები3)

# 11. კონდიცია
რიცხვები4 = [15.5, 3.2, 8.7]
if რიცხვები4[0] > 10:
    რიცხვები4[2] = რიცხვები4[0] * რიცხვები4[1]  # მესამე = პირველი*მეორე
else:
    რიცხვები4[0] = რიცხვები4[1] * რიცხვები4[2]  # პირველი = მეორე*მესამე
print("11)", რიცხვები4)


